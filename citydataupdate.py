def showcityupdate():
    t = tkinter.Tk()
    t.geometry('700x700')
    t.title("CityData")
    t.configure(bg='#93C572')
    def updatedata():
        db = pymysql.connect(host='localhost', user='root', password='root', database='cms')
        cur = db.cursor()
        xa = int(e1.get())
        xb = e2.get()
        xc = e3.get()
        xd = int(e4.get())
        sql = "update citydata set city_name=%s, state=%s, pincode=%s where cityid=%s"
        val = (xb, xc, xd, xa)
        cur.execute(sql, val)
        db.commit()
        db.close()
        messagebox.showinfo("Hi", "Updated")
        e1.delete(0, 100)
        e2.delete(0, 100)
        e3.delete(0, 100)
        e4.delete(0, 100)
