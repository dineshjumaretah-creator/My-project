        db = pymysql.connect(host='localhost', user='root', password='root', database='cms')
        cur = db.cursor()
        xa = int(e1.get())
        sql = "select city_name, state, pincode from citydata where cityid = %s"
        cur.execute(sql, (xa,))
        data = cur.fetchone()
        db.close()
        e2.delete(0, END)
        e3.delete(0, END)
        e4.delete(0, END)
    
        e2.insert(0, data[0])
        e3.insert(0, data[1])
